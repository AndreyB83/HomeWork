string_1 = str(input('Введите строку:'))
print('Третий символ строки:', string_1[2])
print('Предпоследний символ строки:', string_1[-2])
print('Первые пять символов строки:', string_1[:5])
print('Строка без последних двух символов:', string_1[:-2])
print('Символы с четным индексом строки:', string_1[::2])
print('Символы с нечетным индексом строки:', string_1[1::2])
print('Символы строки в обратном порядке:', string_1[::-1])
print('Символы строки в обратном порядке через один:', string_1[::-2])
print('Длина строки:', len(string_1))